CREATE INDEX idx_member_alias_member_id ON member_alias(member_id);
CREATE INDEX idx_member_alias_normalized_text ON member_alias(normalized_text);

-- 12. call_type_master
CREATE TABLE call_type_master (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    site_id BIGINT NOT NULL,
    call_type_code VARCHAR(30) NOT NULL,
    call_type_label VARCHAR(100) NOT NULL,
    color_hex VARCHAR(20),
    icon_key VARCHAR(50),
    description VARCHAR(255),
    display_order INT NOT NULL DEFAULT 0,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_ctm_site FOREIGN KEY (site_id) REFERENCES site(id),
    CONSTRAINT uk_call_type UNIQUE (site_id, call_type_code),
    CONSTRAINT uk_call_type_code UNIQUE (call_type_code)
);
CREATE INDEX idx_call_type_site_id ON call_type_master(site_id);

-- 13. song_call_block
CREATE TABLE song_call_block (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    song_id BIGINT NOT NULL,
    block_type VARCHAR(30) NOT NULL DEFAULT 'VERSE',
    block_label VARCHAR(50),
    order_no INT NOT NULL DEFAULT 0,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_scb_song FOREIGN KEY (song_id) REFERENCES song(id)
);
CREATE INDEX idx_call_block_song_id ON song_call_block(song_id);
CREATE INDEX idx_call_block_order ON song_call_block(song_id, order_no);

-- 14. song_call_line
CREATE TABLE song_call_line (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    block_id BIGINT NOT NULL,
    line_no INT NOT NULL DEFAULT 0,
    lyrics TEXT NOT NULL,
    note VARCHAR(500),
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_scl_block FOREIGN KEY (block_id) REFERENCES song_call_block(id),
    CONSTRAINT uk_call_line UNIQUE (block_id, line_no)
);
CREATE INDEX idx_call_line_block_id ON song_call_line(block_id);
CREATE INDEX idx_call_line_block_line ON song_call_line(block_id, line_no);

-- 15. song_call_item
CREATE TABLE song_call_item (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    call_line_id BIGINT NOT NULL,
    call_type_code VARCHAR(30) NOT NULL,
    call_text VARCHAR(500) NOT NULL,
    style_color_hex VARCHAR(20),
    order_no INT NOT NULL DEFAULT 0,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_sci_line FOREIGN KEY (call_line_id) REFERENCES song_call_line(id),
    CONSTRAINT fk_sci_call_type FOREIGN KEY (call_type_code) REFERENCES call_type_master(call_type_code)
);
CREATE INDEX idx_call_item_line_id ON song_call_item(call_line_id);
CREATE INDEX idx_call_item_type ON song_call_item(call_type_code);
CREATE INDEX idx_call_item_line_order ON song_call_item(call_line_id, order_no);
